
package snakepractice;


public class Posicion {
    public int x, y;
    public char tipo;
    
    public Posicion(){}
    public Posicion(int x, int y, char tipo){
        this.x = x;
        this.y = y;
        this.tipo = tipo;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public char getTipo() {
        return tipo;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setTipo(char tipo) {
        this.tipo = tipo;
    }
    
    
    
                                                                            
    
    
    
}
